const axios = require('axios');

async function testProxies(proxies) {
    for (const proxy of proxies) {
        const [host, port] = proxy.host.split(':');
        try {
            const testRes = await axios.create({
                proxy: {
                    host: host,
                    port: parseInt(port),
                    auth: {
                        username: proxy.username,
                        password: proxy.password
                    }
                }
            }).get('http://ipinfo.io/ip');
            console.log(`Proxy ${host}:${port} is working. IP: ${testRes.data}`);
        } catch (error) {
            console.error(`Proxy ${host}:${port} failed. Error: ${error.message}`);
        }
    }
}

testProxies(proxies);